CREATE DATABASE facturation;
USE facturation;

-- Suppression des tables
DROP TABLE IF EXISTS DETAILS_COMMANDES;
DROP TABLE IF EXISTS COMMANDES;
DROP TABLE IF EXISTS PRODUITS;
DROP TABLE IF EXISTS CLIENTS;

-- Structure de la table `CLIENTS`
CREATE TABLE CLIENTS (
  CODE_CLIENT char(5) NOT NULL,
  SOCIETE varchar(40) NOT NULL,
  ADRESSE varchar(60) NOT NULL,
  VILLE varchar(30) NOT NULL,
  CODE_POSTAL varchar(10) NOT NULL,
  PAYS varchar(15) NOT NULL,
  CATEGORIE int(2) NOT NULL,
  PRIMARY KEY (CODE_CLIENT)
);

-- Structure de la table `PRODUITS`
CREATE TABLE PRODUITS (
  REF_PRODUIT int(6) NOT NULL,
  NOM_PRODUIT varchar(40) UNIQUE NOT NULL,
  PRIX_UNITAIRE float(8,2) NOT NULL CHECK (PRIX_UNITAIRE>=0),
  PRIMARY KEY (REF_PRODUIT)
);

-- Structure de la table `COMMANDES`
CREATE TABLE COMMANDES (
  NO_COMMANDE int(6) NOT NULL,
  DATE_COMMANDE date NOT NULL,
  CODE_CLIENT char(5) NOT NULL,
  PRIMARY KEY (NO_COMMANDE),
  FOREIGN KEY (CODE_CLIENT) REFERENCES CLIENTS (CODE_CLIENT)
);

-- Structure de la table `DETAILS_COMMANDES`
CREATE TABLE DETAILS_COMMANDES (
  NO_COMMANDE int(6) NOT NULL,
  REF_PRODUIT int(6) NOT NULL,
  QUANTITE int(5) NOT NULL DEFAULT 1,
  PRIMARY KEY (NO_COMMANDE,REF_PRODUIT),
  FOREIGN KEY (NO_COMMANDE) REFERENCES COMMANDES (NO_COMMANDE),
  FOREIGN KEY (REF_PRODUIT) REFERENCES PRODUITS (REF_PRODUIT)
);


